CREATE TABLE IF NOT EXISTS `exchange_order` (
  `id` bigint NOT NULL AUTO_INCREMENT,
  `order_id` varchar(255) NOT NULL COMMENT 'order id',
  `amount` decimal(18,8) NOT NULL DEFAULT 0.00000000 COMMENT 'order amount',
  `base_symbol` varchar(255) NOT NULL COMMENT 'base settlement symbol',
  `canceled_time` bigint NOT NULL DEFAULT 0 COMMENT 'cancel time',
  `coin_symbol` varchar(255) NOT NULL COMMENT 'coin symbol',
  `completed_time` bigint NOT NULL DEFAULT 0 COMMENT 'completed time',
  `direction` int NOT NULL COMMENT '0 buy, 1 sell',
  `member_id` bigint NOT NULL,
  `price` decimal(18,8) NOT NULL DEFAULT 0.00000000 COMMENT 'order price',
  `status` int NOT NULL COMMENT '0 trading, 1 completed, 2 canceled, 3 overtime, 4 init',
  `symbol` varchar(255) NOT NULL COMMENT 'trade symbol',
  `time` bigint NOT NULL DEFAULT 0 COMMENT 'create time',
  `traded_amount` decimal(18,8) NOT NULL DEFAULT 0.00000000 COMMENT 'traded amount',
  `turnover` decimal(18,8) NOT NULL DEFAULT 0.00000000 COMMENT 'turnover',
  `type` int NOT NULL COMMENT '0 market, 1 limit',
  `use_discount` varchar(255) NOT NULL DEFAULT '0' COMMENT 'discount flag',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_exchange_order_order_id` (`order_id`),
  KEY `idx_exchange_order_member_time` (`member_id`, `time`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb3 COLLATE=utf8mb3_general_ci;
